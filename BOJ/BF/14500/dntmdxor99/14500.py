read = lambda : map(int, input().split())
n, m = read()
maps = [list(read()) for _ in range(n)]

figs = [[(0, 1), (0, 2), (0, 3)],
       [(1, 0), (2, 0), (3, 0)],
       [(1, 0), (0, 1), (1, 1)],
       [(1, 0), (2, 0), (2, 1)],
       [(1, 0), (2, 0), (2, -1)],
       [(0, 1), (0, 2), (1, 2)],
       [(0, 1), (0, 2), (-1, 2)],
       [(-1, 0), (-2, 0), (-2, -1)],
       [(-1, 0), (-2, 0), (-2, 1)],
       [(0, -1), (0, -2), (-1, -2)],
       [(0, -1), (0, -2), (1, -2)],
       [(1, 0), (1, -1), (2, -1)],
       [(1, 0), (1, 1), (2, 1)],
       [(0, 1), (-1, 1), (-1, 2)],
       [(0, 1), (1, 1), (1, 2)],
       [(0, 1), (1, 1), (0, 2)],
       [(0, 1), (-1, 1), (0, 2)],
       [(1, 0), (1, 1), (2, 0)],
       [(1, 0), (1, -1), (2, 0)],
       ]

ma = 0
for i in range(n):
    for j in range(m):
        for fig in figs:
            sum = maps[i][j]
            for f in fig:
                if 0 <= (ni := i + f[0]) < n and 0 <= (nj := j + f[1]) < m:
                    sum += maps[ni][nj]
                else:
                    break
            else:
                ma = max(ma, sum)
print(ma)